	{
		u32 r31;
		switch (param_2 & 0xff) {
		case 0x15:
		case 0x17:
		case 0x1D:
			r31 = MSD_SE_MA_STALL_JUMP;
			break;

		case 0x1E:
		default:
			if (param_4 < 0x1770)
				r31 = MSD_SE_MA_ROPE_JUMP_A;
			else if (param_4 < 0x2EE0)
				r31 = MSD_SE_MA_ROPE_JUMP_B;
			else
				r31 = MSD_SE_MA_ROPE_JUMP_C;
			break;
		}

		if (gateCheck(r31))
			startSoundActor(r31, param_1, 0, nullptr, 0, 4);
	}

